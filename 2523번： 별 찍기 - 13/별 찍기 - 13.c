#include <stdio.h>

int main()
{
    int a, i, j, ii;
    scanf("%d", &a);
    for (i = 0; i < a - 1; i++)
    {
        for (j = 0; j <= i; j++)
        {
            printf("*");
        }
        printf("\n");
    }
    for (i = a - 1; i >= 0; i--)
    {
        for (j = 0; j <= i; j++)
        {
            printf("*");
        }
        printf("\n");
    }
}