#include <stdio.h>
int main()
{
    int a = 0, b = 0, g = 0, h = 0, k = 0, l = 0;
    scanf("%d %d", &a, &b);
    scanf("%d %d", &g, &h);
    scanf("%d %d", &k, &l);
    if (a == g)
        a = 0, g = 0;
    if (b == h)
        b = 0, h = 0;
    if (a == k)
        a = 0, k = 0;
    if (b == l)
        b = 0, l = 0;
    printf("%d %d\n", a ? a : g ? g
                                : k,
           b ? b : h ? h
                     : l);
}