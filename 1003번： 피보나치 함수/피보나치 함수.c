// 아 ㅋㅋ 노가다
#include <stdio.h>
int main()
{
    long long a[40][2];
    int T, n, i;
    a[0][0] = 1, a[0][1] = 0;
    a[1][0] = 0, a[1][1] = 1;
    scanf("%d", &T);
    long long s[T][2];
    for (int j = 0; j < T; j++)
    {
        scanf("%d", &n);
        if (n <= 1)
            s[j][0] = a[n][0], s[j][1] = a[n][1];
        else
        {
            for (i = 2; i <= n; i++)
                a[i][0] = a[i - 1][1], a[i][1] = a[i - 1][0] + a[i - 1][1];
            s[j][0] = a[i - 1][0], s[j][1] = a[i - 1][1];
        }
    }
    for (i = 0; i < T; i++)
    {
        printf("%lld %lld\n", s[i][0], s[i][1]);
    }
}