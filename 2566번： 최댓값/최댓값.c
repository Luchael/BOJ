#include <stdio.h>

int main()
{
    int max = 0, k = 0, index = 0;
    for (int i = 0; i < 81; i++)
    {
        scanf("%d", &k);
        if (max < k)
        {
            max = k;
            index = i;
        }
    }
    printf("%d\n%d %d\n", max, index / 9 + 1, index % 9 + 1);
}