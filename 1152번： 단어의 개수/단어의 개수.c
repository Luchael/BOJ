#include <stdio.h>
int main()
{
    char a = 0, b;
    int r = 0;
    scanf("%c", &a);
    while (1)
    {
        b = a;
        scanf("%c", &a);
        if (b != 0 && a == ' ')
            r++;
        if (a == '\n')
        {
            if (b == ' ')
                r--;
            break;
        }
    }
    printf("%d", ++r);
}