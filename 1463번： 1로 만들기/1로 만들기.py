N = int(input())+1
dp=[i-1 for i in range(N)]
for i in range(2,N):
    for j in 2,3:
        if i%j==0:
            dp[i]=min(dp[i],dp[i//j]+1)
    dp[i]=min(dp[i],dp[i-1]+1)
print(dp[N-1])