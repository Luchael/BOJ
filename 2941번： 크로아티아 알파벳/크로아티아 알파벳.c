#include <stdio.h>
int main()
{
    int tf = 0, r = 0;
    char a = 0, b;
    while (1)
    {
        b = a;
        scanf("%c", &a);
        if (a == '\n')
            break;
        if (((b == 'l' || b == 'n') && a == 'j') || a == '=' || a == '-')
        {
            tf = 0;
            continue;
        }
        if (tf)
            tf = 0, r++;
        if (b == 'd' && a == 'z')
        {
            tf++;
            continue;
        }
        r++;
    }
    printf("%d", tf ? ++r : r);
}