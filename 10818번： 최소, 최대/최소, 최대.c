#include <stdio.h>
int main()
{
    int N, min, max, a;
    scanf("%d", &N, min = 1000001, max = -1000001);
    for (int i = 0; i < N; i++)
    {
        scanf("%d", &a), min = min > a ? a : min, max = max < a ? a : max;
    }
    printf("%d %d", min, max);
}