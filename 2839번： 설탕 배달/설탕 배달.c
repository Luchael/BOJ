#include <stdio.h>

int main()
{
    int N;
    scanf("%d", &N);
    if (N < 8)
        return (printf("%d", N % 3 ? (N % 5 ? -1 : N / 5) : N / 3), 0);
    if (N % 5)
    {
        switch (N % 5 % 2)
        {
        case 1:
            N = N / 5 + 1;
            break;
        default:
            N = N / 5 + 2;
            break;
        }
        printf("%d", N < 0 ? (-1) : N);
    }
    else
        printf("%d", N / 5);
}