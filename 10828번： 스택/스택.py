l = -1
k = [0]*10001
for i in [*open(0)][1:]:
    i=i.split()
    if i[0] == 'push':
        l+=1
        k[l] = i[1]
    elif i[0] == 'size':
        print(l+1)
    elif i[0] == 'empty':
        print([0,1][l==-1])
    elif l == -1:
        print(-1)
    else:
        if i[0] == 'pop':
            print(k[l])
            l-=1            
        elif i[0] == 'top':
            print(k[l])