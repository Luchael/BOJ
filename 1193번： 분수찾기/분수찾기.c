#include <stdio.h>
int main()
{
    int a, b = 1, c = 1, tmp;
    scanf("%d", &a);
    for (a; a > 1; a--)
    {
        if (b == 1 && c % 2)
            c++;
        else if (c == 1 && !(b % 2))
            b++;
        else
        {
            if ((b + c) % 2)
                b++, c--;
            else
                b--, c++;
        }
    }
    printf("%d/%d", b, c);
}