#include <stdio.h>

int main()
{
    int T, k, n, i;
    scanf("%d", &T);
    for (; T--; T > 0)
    {
        scanf("%d", &k);
        scanf("%d", &n);
        int q[n + 1];
        for (i = n; n > 0; n--)
        {
            q[n] = n * (n + 1) / 2;
        }
        for (; k > 1; k--)
        {
            for (n = i; n > 0; n--)
            {
                for (int j = n - 1; j > 0; j--)
                {
                    q[n] += q[j];
                }
            }
        }
        printf("%d\n", q[i]);
    }
}