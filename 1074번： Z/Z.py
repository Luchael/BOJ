a = 0
def ABCD(n, r, c):
	global a
	if n==0: 
		print(a)
		return 0
	if r<2**(n-1):
		if c<2**(n-1):
			a+=0*4**(n-1)
		else:
			c-=2**(n-1)
			a+=1*4**(n-1)
	else:
		r-=2**(n-1)
		if c<2**(n-1):
			a+=2*4**(n-1)
		else:
			c-=2**(n-1)
			a+=3*4**(n-1)
	ABCD(n-1, r, c)
	
ABCD(*map(int,[*open(0)][0].split()))