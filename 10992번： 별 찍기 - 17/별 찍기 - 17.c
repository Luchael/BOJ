#include <stdio.h>

int main()
{
    int a, i, j;
    scanf("%d", &a);
    for (i = 0; i < a; i++)
    {
        for (j = 0; j < a + i; j++)
        {
            if (i != a - 1)
            {
                if (j == a + i - 1 || j == a - i - 1)
                    printf("*");
                else
                    printf(" ");
            }
            else
                printf("*");
        }
        printf("\n");
    }
}