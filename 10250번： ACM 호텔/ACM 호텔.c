#include <stdio.h>

int main()
{
    int T, h, w, n;
    scanf("%d", &T);
    for (; T > 0; T--)
    {
        scanf("%d %d %d", &h, &w, &n);
        w = n % h, n /= h;
        printf("%d%02d\n", w ? w : w + h, w ? n + 1 : n);
    }
}