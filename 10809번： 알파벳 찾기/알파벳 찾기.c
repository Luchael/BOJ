int main()
{
    char a, b[26], i = 0;
    for (int i = 0; i < 26; i++)
        b[i] = -1;
    while (scanf("%c", &a))
    {
        if (a == '\n')
            break;
        a -= 'a', a < 0 ? (a -= ('A' - 'a')) : a;
        b[a] == -1 ? b[a] = i : b[a], i++;
    }
    for (int i = 0; i < 26; i++)
        printf("%d ", b[i]);
}